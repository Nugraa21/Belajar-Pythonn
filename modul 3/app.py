a, b = 10, 3  # menyimpan dua nilai sekaligus

# Operator Aritmatika
print(a + b, a - b, a * b, a / b, a % b, a ** b)

# Operator Perbandingan
print(a == b, a != b, a > b, a < b)

# Operator Logika
print(a > 5 and b < 5)  # True jika dua kondisi benar
print(a > 5 or b > 10)  # True jika salah satu benar