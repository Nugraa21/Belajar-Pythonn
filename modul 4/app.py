nilai = int(input("Masukkan nilai kamu: "))

if nilai >= 90:
    print("A — Sangat Baik!")
elif nilai >= 75:
    print("B — Bagus!")
elif nilai >= 60:
    print("C — Cukup!")
else:
    print("D — Kurang!")